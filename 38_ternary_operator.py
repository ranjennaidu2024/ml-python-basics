# Ternary Operator - same as if else but shortcut to make your code clean
# can also know as ternary expression , from python 2.4 onwards

# condition_if_true if condition else condition_if_else

# if is_friend is true will print assign can_message to the left , else to the right
is_friend = False
can_message = "message allowed" if is_friend else "not allowed to message"

print(can_message)